"use strict";
// In TypeScript, you can manipulate strings using various methods.
// all string methods in TypeScript return a new string, leaving the original unchanged
const stringData = "The City That Never Sleeps\n";
console.log(stringData.toLowerCase()); // Converts a string to lowercase
console.log(stringData.toUpperCase()); //  Converts a string to uppercase
console.log(stringData.trim()); // removed line terminator characters from a string
console.log(stringData.substring(3, 7)); // returns the substring that includes the characters
